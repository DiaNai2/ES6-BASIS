//manera sintaxis flecha de escribir componentes funcionales
export const sumar = (a, b) => a + b;

//sintaxis identificador
export const restar = function(a, b) {
    return a - b
}

//sintaxis legada
export function multiplicar(a, b) {
    return a * b
}

export const dividir = (a, b) => a/b;

export const exponente = (a, b) => Math.pow(a, b);
